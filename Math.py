import math


pi = math.pi
e = math.e


def cos(x):
    return math.cos(x)


def acos(x):
    return math.acos(x)


def acosh(x):
    return math.acosh(x)


def sin(x):
    return math.sin(x)


def asin(x):
    return math.asin(x)


def asinh(x):
    return math.asinh(x)


def tan(x):
    return math.tan(x)


def atan(x):
    return math.atan(x)


def atan2(y, x):
    return math.atan2(y, x)


def atanh(x):
    return math.atanh(x)


def radians(x):
    return math.radians(x)


def degrees(x):
    return math.degrees(x)


def sqrt(x):
    return math.sqrt(x)


def log(x, base):
    return math.log(x, base=base)


def log2(x):
    return math.log2(x)


def log10(x):
    return math.log10(x)


def ln(x):
    return math.log(x, base=math.e)


def factorial(x):
    return math.factorial(x)


def max(a, b):
    return max(a, b)


def abs(x):
    return abs(x)
